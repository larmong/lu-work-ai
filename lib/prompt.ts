/**
 * AI 응답 content 후처리
 * 혹시 AI가 규칙을 안 지킬 경우를 대비한 안전장치
 */
export function postProcessContent(content: string): string {
  let processed = content;

  // 1. 온점 제거 (단, 번호 뒤의 온점은 유지: "1. ", "2. " 등)
  // 먼저 번호 뒤의 온점을 임시 마커로 변경
  processed = processed.replace(/^(\d+)\.\s/gm, '___NUMBER_$1___SPACE___');
  
  // 문장 끝 온점 제거 (한글 뒤의 온점)
  processed = processed.replace(/([가-힣])\.(\s+|$)/g, '$1$2');
  
  // 영어나 숫자가 아닌 문자 뒤의 온점 제거
  processed = processed.replace(/([^0-9a-zA-Z])\.(\s+|$)/g, '$1$2');
  
  // 임시 마커를 다시 번호 형식으로 복원
  processed = processed.replace(/___NUMBER_(\d+)___SPACE___/g, '$1. ');

  // 2. 너무 격식 있는 표현 → 정중한 존댓말로 변환
  const tooFormalToPolite: Record<string, string> = {
    '입니다': '이에요',
    '습니다': '해요',
    '합니다': '해요',
    '됩니다': '돼요',
    '있습니다': '있어요',
    '없습니다': '없어요',
    '드립니다': '드려요',
  };

  Object.entries(tooFormalToPolite).forEach(([tooFormal, polite]) => {
    const regex = new RegExp(tooFormal, 'g');
    processed = processed.replace(regex, polite);
  });

  // 3. 반말 → 존댓말 변환 (혹시 반말이 들어갔을 경우)
  const informalToPolite: Record<string, string> = {
    '라네': '라네요',
    '하네': '하네요',
    '인 듯': '인 것 같아요',
    '같음': '같아요',
    '좋음': '좋아요',
    '나쁨': '나빠요',
    '추천함': '추천해요',
    '해봄': '해봤어요',
    '써봄': '써봤어요',
    '가봄': '가봤어요',
    '먹어봄': '먹어봤어요',
  };

  // 문장 끝 반말 패턴 변환 (공백이나 줄바꿈 앞에 있는 경우)
  Object.entries(informalToPolite).forEach(([informal, polite]) => {
    const regex = new RegExp(informal + '(?=\\s|$|!)', 'g');
    processed = processed.replace(regex, polite);
  });

  // 4. 마크다운 문법 제거 (단, 번호 매기기는 유지)
  // 헤더 제거 (##, ###)
  processed = processed.replace(/^#{1,6}\s+/gm, '');
  
  // 볼드 제거 (**text** -> text)
  processed = processed.replace(/\*\*(.+?)\*\*/g, '$1');
  
  // 이탤릭 제거 (*text* -> text)
  processed = processed.replace(/\*(.+?)\*/g, '$1');
  
  // 리스트 마커 제거 (-, * 만, 번호 리스트는 유지)
  processed = processed.replace(/^[\s]*[-*]\s+/gm, '');
  
  // 코드 블록 제거 (```...```)
  processed = processed.replace(/```[\s\S]*?```/g, '');
  
  // 인라인 코드 제거 (`text` -> text)
  processed = processed.replace(/`(.+?)`/g, '$1');
  
  // 인용문 제거 (>)
  processed = processed.replace(/^>\s+/gm, '');

  // 5. 여러 개의 연속된 빈 줄을 최대 3개로 제한
  processed = processed.replace(/\n{4,}/g, '\n\n\n');

  return processed.trim();
}

/**
 * title 후처리
 */
export function postProcessTitle(title: string): string {
  let processed = title;
  // 마크다운 제거
  processed = processed.replace(/^#{1,6}\s+/, '');
  processed = processed.replace(/\*\*(.+?)\*\*/g, '$1');
  // 온점 제거
  processed = processed.replace(/\.$/, '');
  return processed.trim();
}

/**
 * outline 배열 후처리
 */
export function postProcessOutline(outline: string[]): string[] {
  return outline.map((item, idx) => {
    let processed = item;
    // 마크다운 헤더 제거
    processed = processed.replace(/^#{1,6}\s+/, '');
    
    // 번호가 없으면 추가, 있으면 유지
    if (!/^\d+\.\s/.test(processed)) {
      processed = `${idx + 1}. ${processed}`;
    }
    
    return processed.trim();
  });
}
